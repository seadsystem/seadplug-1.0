uint16_t get_celcius(uint16_t temperature_reading) {
	uint16_t temp_index = (temperature_reading-TEMP_OFFSET > TEMP_CAP)? TEMP_CAP : temperature_reading - TEMP_OFFSET;
	return temp_lookup_table[temp_index];
}


